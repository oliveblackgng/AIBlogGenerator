print(len(lc)) # gives the number of characters in the string
print(lc[6]) # 012345... gives character of given index
print(lc.index(" BR")) # gives the index of the character that is just before the string
print(lc.replace("hate BRED","is BLACK")) # replaces old string w/ a new one